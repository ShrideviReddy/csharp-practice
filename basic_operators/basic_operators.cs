using System;

namespace  basic_operators{


	class basic_operators{
	
	
		static void Main(string[] args)
		{
		
		// Add operation
		int sum = 5 + 5;

		// Substract operation
		int minus = 5 - 3;

		// Multiplication operation
		int product = 5 * 3;

		//Divide operation
		float quotient = 4/2;

		Console.WriteLine("Operation Results");

		Console.WriteLine("5 + 5: " + sum);
		Console.WriteLine("5 - 3: " + minus);
		Console.WriteLine("5 * 3: " + product);
		Console.WriteLine("4 / 2: " + quotient);
		}	
	
	}

}
